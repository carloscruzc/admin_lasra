    $(document).ready(function() {

        $('#comprobantes a').addClass('active');
        $('#comprobantes .fa-receipt').addClass('text-white');

        $(".btn_ver_comprobante").on("click", function() {
            var url_archivo = $(this).attr('data-url-archivo');
            var user_id = $(this).attr('data-user-id');
            $("#iframe_comprobante").attr('src', 'http://localhost:8124/comprobantesPago/' + url_archivo);
            $("#_url_archivo").val(url_archivo);
            $("#user_id").val(user_id);
        })

        $("#btn_aceptar").on("click", function() {
            var url_archivo = $("#_url_archivo").val();
            var user_id = $("#user_id").val();
            var status = 1;
            var msg = "Se va a aceptar el comprobante";

            cambiarStatus(url_archivo,status,user_id,msg);

            // alert(url_archivo);


        })

        $("#btn_rechazar").on("click", function() {
            var url_archivo = $("#_url_archivo").val();
            var user_id = $("#user_id").val();
            var status = 2;
            var msg = "Se va a rechazar el comprobante";

            cambiarStatus(url_archivo,status,user_id,msg);
            // alert(url_archivo);
        })


        function cambiarStatus(url_archivo,status,user_id,msg) {
            Swal.fire({
                title: msg,
                text: "",
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                cancelButtonText: 'Cancelar',
                confirmButtonText: 'Si'
            }).then((result) => {
                if (result.isConfirmed) {
                    $.ajax({
                        url: "/ComprobantesPago/cambiarStatus",
                        type: "POST",
                        data: {
                            url_archivo,status,user_id
                        },
                        // dataType: 'json',
                        beforeSend: function() {
                            console.log("Procesando....");

                        },
                        success: function(respuesta) {

                            console.log(respuesta);

                            if(respuesta == 'success'){
                                Swal.fire('Operaci칩n realizada exitosamente','','success');
                                setTimeout(() => {
                                    location.reload();
                                }, 2000);
                            }else{
                                Swal.fire('Hubo un error al realizar la operaci칩n','','success');
                            }


                        },
                        error: function(respuesta) {
                            console.log(respuesta);
                        }

                    });
                }

            })
        }

    })
</script>

